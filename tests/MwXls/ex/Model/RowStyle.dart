class RowStyle {
  final String comment;
  final double height;
  final bool customFormat;
  final bool customHeight;
  final bool hidden;
  final bool collapsed;

  RowStyle({
    this.comment = '',
    this.height = 12.1,
    this.customFormat = false,
    this.customHeight = false,
    this.hidden = false,
    this.collapsed = false,
  });
}
